// Rastgele sayı üretme (0 - 100 arası)
let randomNumber = Math.floor(Math.random() * 101);
console.log("Rastgele Sayı:", randomNumber);

// Sayıyı yukarı yuvarlama
let ceilNumber = Math.ceil(4.2);
console.log("Yukarı Yuvarlama:", ceilNumber);

// Sayıyı aşağı yuvarlama
let floorNumber = Math.floor(4.8);
console.log("Aşağı Yuvarlama:", floorNumber);

// Sayıyı en yakın tam sayıya yuvarlama
let roundNumber = Math.round(4.5);
console.log("Normal Yuvarlama:", roundNumber);

// En büyük sayıyı bulma
let maxNumber = Math.max(10, 20, 5, 8);
console.log("En Büyük Sayı:", maxNumber);

// En küçük sayıyı bulma
let minNumber = Math.min(10, 20, 5, 8);
console.log("En Küçük Sayı:", minNumber);

// Mutlak değer alma
let absNumber = Math.abs(-25);
console.log("Mutlak Değer:", absNumber);
